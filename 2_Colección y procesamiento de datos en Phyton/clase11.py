# TABLERO DE AJEDREZ

#Un tablero de ajedrez es una matriz de 8x8. En vez de representar solo las casillas
#blancas y negras, podemos representar las piezas de ajedrez. Usaremos letras para
#representar las piezas: P para peón, R para torre, N para caballo (knight), B para alfil, Q
#para reina y K para rey. Las piezas negras se representan con letras minúsculas y las
#blancas con letras mayúsculas.


print("Este es un ejemplo de aplicación de matrice, tablero de ajedrez.")

